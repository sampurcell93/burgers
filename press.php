		<div class="head"><h2>Meet Mainely Burgers!</h2>
		<h3>Food Truck Festivals of New England - March 2013</h3></div>
		<p>Ben Berman and Jack Barber, the co-proprietors of Mainely Burgers food trucks, grew up together in a small town just outside of Portland, ME. When they got to school in Boston, on opposite sides of the city, they stay connected by meeting at restaurants and then food trucks. The trucks combined their love of food with their
		interest in business. It didn't seem right that there were none at home in Portland, ME.</p>
		<p>"We joke that, for a while, our business plan was just a list of menu items we were throwing around," said Barber.</p>
		<p>"We each had a little experience in kitchens, but we really just shared a love of food and loved the idea of creating a business around that. Nine months later, we were serving our first burger," said Berman.</p>
